// Похідний клас Журнал
class Журнал : ДрукарськеВидання
{
    public string Періодичність { get; set; }
    public int КількістьСпівробітників { get; set; }

    public Журнал(string назва, double ціна, string періодичність, int кількістьСпівробітників)
        : base(назва, ціна)
    {
        Періодичність = періодичність;
        КількістьСпівробітників = кількістьСпівробітників;
    }

    // Перевизначені методи
    public override void ПоказатиДані()
    {
        base.ПоказатиДані();
        Console.WriteLine($"Періодичність: {Періодичність}, Кількість Співробітників: {КількістьСпівробітників}");
    }

    // Власний метод класу з параметрами
    public void ПоказатиДеталі(string періодичність)
    {
        Console.WriteLine($"Назва: {Назва}, Періодичність: {періодичність}, Кількість Співробітників: {КількістьСпівробітників}");
    }

    // Метод для обчислення плинності кадрів
    public double ОбчислитиПлинність()
    {
        int[] кількістьСпівробітниківЗа3Місяці = { 90, 85, 80 }; // Приклад масиву
        double плинність = кількістьСпівробітниківЗа3Місяці.Average();
        return плинність;
    }
}
